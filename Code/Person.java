package gofoo;

/**
 * A parent class that has the personal information of a player or playground owner as its attributes in which class Player or
 * playgroundOwner will inherit these attributes when an objects from them are created by the user.
 * @author Adel Ashraf
 * @version 1.0 June 8, 2021
 */
public class Person {
    protected String name;
    protected String age;
    protected String nationalId;
    protected String phoneNo;
    protected String address;
    protected String sex;
    protected String userName;
    protected String email;
    protected String password;
}